public func ePrint(_ items: Any...) {
    print("🆘", items, "🆘")
}

public func wPrint(_ items: Any...) {
    print("⚠️", items, "⚠️")
}

public func iPrint(_ items: Any...) {
    print("☺️", items, "☺️")
}
